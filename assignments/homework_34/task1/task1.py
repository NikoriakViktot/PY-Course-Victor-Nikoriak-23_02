# Threading. Додайте до класу дві глобальні змінні: одну під назвою counter із значенням
# 0, а іншу — rounds із значенням 100 000. Тепер реалізуйте метод run(), нехай він
# містить простий цикл for, який проходить по rounds (тобто 100 000 разів) і кожного
# разу збільшує значення лічильника на 1. Створіть 2 екземпляри потоку та запустіть їх,
# потім об'єднайте їх і перевірте результат лічильника, він повинен дорівнювати 200 000,
# чи не так? Запустіть програму кілька разів і розгляньте різні причини, чому ви
# отримуєте саме такий результат.
import threading
# Глобальні змінні згідно з умовою завдання
counter = 0
rounds = 100000
class Counter(threading.Thread):
    def run(self):
        global counter, rounds
        # Простий цикл, який інкрементує лічильник
        for _ in range(rounds):
            counter += 1
# Запуск експерименту
if __name__ == "__main__":
    print(f"Початкове значення лічильника: {counter}")
    print(f"Очікуване значення після роботи 2 потоків: {rounds * 2}\n")
    # Створюємо 2 екземпляри нашого кастомного класу потоку
    thread1 = Counter()
    thread2 = Counter()
    # Запускаємо потоки (це викличе метод run() у кожному з них)
    thread1.start()
    thread2.start()
    # Чекаємо завершення обох потоків
    thread1.join()
    thread2.join()
    # Виводимо фінальний результат
    print(f"🔴 Фінальне значення лічильника (фактичне): {counter}")