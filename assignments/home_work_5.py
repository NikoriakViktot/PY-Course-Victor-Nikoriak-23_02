next_age = age + 1

print(f"Привіт {name}! На наступний день народження тобі виповниться {next_age} років")

# Task 3
# Комбінації слів
# Створіть програму, яка зчитує вхідний рядок, а потім формує та виводить 5 випадкових рядків
# із символів цього вхідного рядка.
# Наприклад, програма отримала слово «hello», тому вона повинна вивести
# 5 випадкових рядків (слів), що поєднують символи
# “h”, “e”, “l”, “l”, “o” -> “hlelo”, “olelh”, “loleh” …
# Підказки: Використовуйте модуль random для отримання випадкового символу з рядка)
import random

word = input("Введіть слово: ")

print(f"5 випадкових комбінацій із літер '{word}':")

for _ in range(5):
    # random.sample повертає список випадкових символів заданої довжини
    char_list = random.sample(word, len(word))
    # ''.join() з'єднує список символів назад у рядок
    random_word = ''.join(char_list)
    print(random_word)