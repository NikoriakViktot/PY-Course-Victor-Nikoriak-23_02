# Програма для привітання з днем народження.#
# Напишіть програму, яка приймає в якості вхідних даних ваше ім’я,
# а потім ваш вік і вітає вас таким повідомленням:#
# «Привіт, <ім’я>! На наступний день народження тобі виповниться <вік+1> років»
name = input("Введіть ваше ім'я: ")
