# Task 1
#Гра «Вгадай число».
#Напишіть програму, яка генерує випадкове число від 1 до 10 і пропонує користувачеві вгадати,
# яке саме число було згенеровано.
# Результат слід вивести користувачеві за допомогою оператора print.
import random

# Генеруємо випадкове число від 1 до 10
secret_number = random.randint(1, 10)
# print(f" {secret_number}")

# Отримуємо відповідь від користувача та перетворюємо її на ціле число (int)
guess = int(input("Вгадайте число від 1 до 10: "))

# Перевіряємо результат
if guess == secret_number:
    print("Вітаю! Ви вгадали число!")
else:
    print(f"Ви не вгадали. Було загадано число {secret_number}.")

