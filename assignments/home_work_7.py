# Task 4
# Створити лист із днями тижня.
# В один рядок (ну або як завжди) створити словник виду: {1: "Monday", 2:...
# Також в один рядок або як вдасться створити зворотний словник {"Monday": 1,

# Створюємо список днів тижня
days = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]

# Словник {номер: день} в один рядок
# enumerate(days, 1) починає відлік з одиниці. Aвтоматично видає пари (1, "Monday"), (2, "Tuesday") і так далі.
direct_dict = {i: day for i, day in enumerate(days, 1)}

# Зворотний словник {день: номер} в один рядок
# Просто міняємо місцями ключ і значення
reverse_dict = {day: i for i, day in direct_dict.items()}

print(direct_dict)
print(reverse_dict)