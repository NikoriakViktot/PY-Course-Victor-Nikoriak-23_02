# Task 1
# Створіть програму, яка приймає на вході речення (рядок) і повертає словник,
# у якому всі унікальні слова виступають ключами, а кількість їхніх вживань — значеннями.
sentence = input("Введіть речення: ")

# Розбиваємо рядок на список слів, ігноруючи зайві пробіли
words = sentence.split()

word_count = {}

for word in words:
    # Очищуємо слово від пунктуації та переводимо в нижній регістр
    clean_word = word.strip('.,!?;:').lower()

    # Збільшуємо лічильник: якщо слова ще немає, беремо 0 і додаємо 1
    word_count[clean_word] = word_count.get(clean_word, 0) + 1

print(word_count)

# Task 2
#Обчисліть загальну вартість запасів, де загальна вартість — це сума ціни товару,
# помноженої на кількість саме цього товару.
# Код повинен повернути словник із сумами цін за видами товарів.
# Вхідні дані:

stock = {
    "banana": 6,
    "apple": 0,
    "orange": 32,
    "pear": 15
}
prices = {
    "banana": 4,
    "apple": 2,
    "orange": 1.5,
    "pear": 3
}
# Обчислюємо суму для кожного товару (ціна * кількість)
# Конструкція {item: ... for item in stock} проходить по всіх ключах словника stock.
# Для кожного ключа (наприклад, "banana") ми множимо його кількість зі словника stock на ціну зі словника prices.
total_prices = {item: stock[item] * prices[item] for item in stock}

print(total_prices)

