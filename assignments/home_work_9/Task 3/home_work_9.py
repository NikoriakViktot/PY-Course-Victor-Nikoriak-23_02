# Task 2
# Модуль sys.
# Список «sys.path» ініціалізується на основі змінної середовища PYTHONPATH. Чи можна змінити
# його зсередини Python? Якщо так, чи вплине це на те, де Python шукає файли модулів?
# Проведіть кілька інтерактивних тестів, щоб це з’ясувати.

import sys
import os

# 1. Створимо окрему папку та файл-модуль всередині неї через код
folder_name = "temp_folder"
file_name = "my_secret_module.py"

if not os.path.exists(folder_name):
    os.makedirs(folder_name)

# Записуємо функцію у новий файл
with open(os.path.join(folder_name, file_name), "w", encoding="utf-8") as f:
    f.write('def secret_function():\n    return "Привіт! Я завантажений з секретної папки!"')

print(f"--- Крок 1: Файл {file_name} створено у папці {folder_name} ---")

# 2. Пробуємо імпортувати (це викличе помилку, бо Python не знає про цю папку)
print("\n--- Крок 2: Спроба імпорту без налаштування sys.path ---")
try:
    import my_secret_module
except ModuleNotFoundError:
    print("Результат: Модуль НЕ знайдено (як і очікувалося).")

# 3. Додаємо шлях до нашої папки в sys.path
# os.path.abspath отримує повний шлях до папки
full_path = os.path.abspath(folder_name)
sys.path.append(full_path)

print(f"\n--- Крок 3: Шлях {full_path} додано до sys.path ---")

# 4. Тепер імпортуємо знову
print("\n--- Крок 4: Спроба імпорту ПІСЛЯ зміни sys.path ---")
import my_secret_module
print(f"Результат: {my_secret_module.secret_function()}")

