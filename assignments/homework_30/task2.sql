-- напишіть запит для виведення імен (first_name, last_name) із використанням псевдонімів «First Name», «Last Name» із таблиці співробітників;
-- напишіть запит для отримання унікального ідентифікатора відділу з таблиці співробітників
-- напишіть запит для отримання всіх даних про співробітників з таблиці співробітників, упорядкованих за іменем у порядку спадання
-- напишіть запит для отримання імен (first_name, last_name), зарплати, PF всіх співробітників (PF розраховується як 12% від зарплати)
-- напишіть запит для отримання максимальної та мінімальної зарплати з таблиці співробітників
-- напишіть запит для отримання місячної зарплати (з точністю до 2 знаків після коми) кожного співробітника
-- 1. Запит для виведення імені та прізвища працівників із використанням псевдонімів
-- Використовуємо оператор AS для призначення зрозумілих назв колонкам "First Name" та "Last Name".
SELECT first_name AS "First Name", last_name AS "Last Name"
FROM employees;
-- 2. Запит для отримання унікальних ідентифікаторів департаментів
-- Ключове слово DISTINCT відсікає дублікати, повертаючи лише унікальні ID департаментів.
SELECT DISTINCT department_id
FROM employees;
-- 3. Запит для отримання всіх деталей про працівників, відсортованих за іменем
-- Використовуємо ORDER BY разом із ключевим словом DESC для сортування за спаданням (від Z до A).
SELECT *
FROM employees
ORDER BY first_name DESC;
-- 4. Запит для отримання імен, прізвищ, зарплати та розрахункового фонду (PF)
-- Колонка PF обчислюється динамічно як 12% від базової зарплати працівника (salary * 0.12).
SELECT first_name, last_name, salary, (salary * 0.12) AS PF
FROM employees;
-- 5. Запит для визначення максимальної та мінімальної зарплати в компанії
-- Застосовуємо агрегатні функції MAX() та MIN() для пошуку граничних значень у таблиці.
SELECT MAX(salary) AS "Maximum Salary", MIN(salary) AS "Minimum Salary"
FROM employees;
-- 6. Запит для розрахунку щомісячної зарплати кожного працівника
-- Оскільки в базі зберігається річна або базова ставка, ділимо її на 12 місяців.
-- Функція ROUND(..., 2) округлює отримане значення до 2 знаків після коми.
SELECT first_name, last_name, ROUND(salary / 12.0, 2) AS "Monthly Salary"
FROM employees;