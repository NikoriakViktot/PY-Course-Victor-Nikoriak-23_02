def shaker_sort(arr: list) -> list:
    """
    Модифіковане сортування бульбашкою (Шейкерне сортування).
    Сортує список на місці та повертає його.
    """
    n = len(arr)
    start = 0
    end = n - 1
    swapped = True

    while swapped:
        # Скидаємо прапорець перед початком проходу
        swapped = False

        # 1. Прохід «вгору» (зліва направо) — штовхаємо максимум в кінець
        for i in range(start, end):
            if arr[i] > arr[i + 1]:
                arr[i], arr[i + 1] = arr[i + 1], arr[i]
                swapped = True

        # Якщо переставлень не було — список уже відсортований
        if not swapped:
            break

        # Зменшуємо верхню межу, бо найбільший елемент уже на своєму місці
        end -= 1

        # Знову скидаємо прапорець для зворотного шляху
        swapped = False

        # 2. Прохід «вниз» (справа наліво) — тягнемо мінімум на початок
        for i in range(end - 1, start - 1, -1):
            if arr[i] > arr[i + 1]:
                arr[i], arr[i + 1] = arr[i + 1], arr[i]
                swapped = True

        # Збільшуємо нижню межу, бо найменший елемент уже на своєму місці
        start += 1

    return arr


'''
   Коли застосування Шейкерного сортування є доречним?
1) Майже відсортовані масиви з поодинокими аномаліями:
Якщо дані вже практично впорядковані, але кілька малих 
елементів опинилися в кінці структури, цей алгоритм 
відпрацює значно швидше за класичну бульбашку. 
Його ефективність у найкращому випадку наближається до лінійного часу \(O(n)\).
2) Обмеженість додаткової пам'яті:Алгоритм сортує масив «на місці» (In-place), 
тобто його просторова складність становить \(O(1)\). 
Він не потребує виділення додаткової пам'яті під нові списки (на відміну від Merge Sort).
'''