# Task 3
# Простий калькулятор.
# Створіть функцію make_operation, яка приймає простий арифметичний оператор як перший параметр (для простоти нехай це будуть лише «+», «-» або «*») та довільну кількість аргументів (лише числа) як другий параметр. Потім поверніть суму або добуток усіх чисел у другому параметрі. Наприклад:
# виклик make_operation(“+”, 7, 7, 2) повинен повернути 16
# виклик make_operation(“-”, 5, 5, -10, -20) повинен повернути 30
# виклик make_operation(“*”, 7, 6) повинен повернути 42

def make_operation(operator, *args):
# Якщо числа не передані, повертаємо 0
    if not args:
        return 0
# Беремо перше число як початковий результат
    result = args[0]
# Проходимо по решті чисел (починаючи з другого)
    for num in args[1:]:
        if operator == '+':
            result += num
        elif operator == '-':
            result -= num
        elif operator == '*':
            result *= num
    return result
# Перевірка:
print(make_operation('+', 7, 7, 2))          # Поверне 16
print(make_operation('-', 5, 5, -10, -20))   # Поверне 30 (5 - 5 - (-10) - (-20) = 0 + 10 + 20)
print(make_operation('*', 7, 6))             # Поверне 42
print(make_operation('+', 14, 21, 8))        # Поверне 43