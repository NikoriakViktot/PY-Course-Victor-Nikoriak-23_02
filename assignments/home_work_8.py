# Створіть просту функцію з назвою favorite_movie, яка приймає рядок із назвою вашого улюбленого
# фільму. Потім функція повинна вивести на екран: «Мій улюблений фільм називається {name}».

# def favorite_movie(name): — ми оголошуємо функцію, яка приймає один аргумент name.
# f-string: за допомогою префікса f перед лапками ми можемо вставити змінну {name} прямо в текст.

def favorite_movie(name):
    print(f"Мій улюблений фільм називається {name}")

# Запитуємо назву у користувача та записуємо в змінну movie_name
